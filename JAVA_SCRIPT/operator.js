//arithmetic operator
console.log(1 + 1);
console.log(3 - 1);
console.log(3 * 3);
console.log(12 / 3);

//assignment operator
// =, +=, -=, /=, *=
var a = 1;
a += 2;
// 1 = 2; -> error
console.log(a);

//comparison operator
console.log(1 == 1); //true
console.log(1 == 2); //false
console.log(1 != 1); //true
console.log(1 != 2); // true
console.log(1 > 2); //false
console.log(1 < 2); //false
console.log(1 === 1); //true
console.log(1 === 2); //false