//loop - 반복문
/* while(조건식){
 *      실행문
 * }
 * */
var i = 0;
while (i < 2) {
    console.log('C1');
    console.log('C2');
    i = i + 1;
}

/*
 * for(초기식; 조건식; 증감식;){
 *      조건식의 결과가 참이면 실행
 * }
 * */

for (var i = 0; i < 2; i++) {
    console.log(i);
}


