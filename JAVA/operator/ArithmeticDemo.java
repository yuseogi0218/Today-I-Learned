package org.opentutorials.javatutorials.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {
		/*
		 * 더하기 +
		 * 빼기 -
		 * 곱하기 *
		 * 나누기 / 
		 * 나머지 % 
		 */
		int result = 1+2;
		System.out.println(result);
		
		result = result -1;
		System.out.println(result);
		
		result = result *2;
		System.out.println(result);
		
		result = result /2;
		System.out.println(result);
		
		result = result + 8;
		result = result % 7;
		System.out.println(result);

	}

}
