package org.opentutorials.javatutorials.loop;

public class ForDemo {

	public static void main(String[] args) {
		/*
		 * for(초기화-1; 종료조건-2; 반복실행-3){
    	 *		반복적으로 실행될 구문-2
    	 *			- 종료조건의 값이 false일 때까지 실행
		 * }
		 */
		 for (int i = 0; i < 10; i++) {
	            System.out.println("Coding Everybody " + i);
	        }

	}

}
