package org.opentutorials.javatutorials.constant;

public class ConstantDemo {

	public static void main(String[] args) {
		// 실수 - double
		double a = 2.2;
		
		// 실수 - float
		//float 형이라는 것을 분명하게 명시해주면 된다.
		float b = 2.2F;
		
		// 정수 - int
		int c = 10;
		
		// 정수 - long
		//long 형이라는 것을 분명하게 명시해주면 된다.
		long d = 2147483648L;
		
		// 정수 - byte
		byte e = 100; //-> 실제 자료형은 int 이다.

	}

}
