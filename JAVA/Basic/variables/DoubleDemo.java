package org.opentutorials.javatutorials.variables;

public class DoubleDemo {

	public static void main(String[] args) {
		// 실수 - double
		double a;
		a = 1.1;
		System.out.println(a+1.1);

		// 실수 > 정수
		double b;
		b = 1;
		System.out.println(b+1);
		
		int c;
		c = 5;
		System.out.println(c);
		// c = 1.1; - 에러 type mismatch
	}

}
